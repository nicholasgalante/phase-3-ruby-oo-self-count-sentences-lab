require 'pry'

class String

  def sentence?
    self.end_with?(".")
  end

  def question?
    self.end_with?("?")
  end

  def exclamation?
    self.end_with?("!")
  end

  def count_sentences
    self.split(/[?.!](?=\s)/).count

  end

end

"This is a test! Are you counting? Yes. No?".count_sentences